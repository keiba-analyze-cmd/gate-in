CREATE TABLE IF NOT EXISTS jrdb_race_entries (
      id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
      race_key text NOT NULL,
      race_date date NOT NULL,
      course_code text NOT NULL,
      course_name text NOT NULL,
      race_number int NOT NULL,
      umaban int NOT NULL,
      horse_name text NOT NULL,
      blood_registration_no text,
      -- KYG: 事前データ
      idm numeric,
      jockey_index numeric,
      info_index numeric,
      sogo_index numeric,
      running_style int,
      distance_aptitude int,
      base_odds numeric,
      base_popularity int,
      training_index numeric,
      stable_index numeric,
      ten_index numeric,
      agari_index numeric,
      position_index numeric,
      pace_prediction text,
      jockey_name text,
      jockey_code text,
      trainer_name text,
      trainer_code text,
      waku int,
      sogo_mark int,
      idm_mark int,
      -- SEC: 結果データ
      distance int,
      surface_code int,
      grade int,
      race_name text,
      head_count int,
      finish_order int,
      anomaly int DEFAULT 0,
      finish_time int,
      final_tansho_odds numeric,
      final_tansho_popularity int,
      result_idm int,
      race_pace text,
      result_ten_index numeric,
      result_agari_index numeric,
      horse_weight int,
      horse_weight_change int,
      -- UKC: 血統データ
      sire_name text,
      dam_name text,
      broodmare_sire_name text,
      sire_system_code text,
      bms_system_code text,
      -- メタ
      created_at timestamptz DEFAULT now(),
      UNIQUE(race_key, umaban)
    );
    CREATE INDEX IF NOT EXISTS idx_jrdb_entries_race_key ON jrdb_race_entries(race_key);
    CREATE INDEX IF NOT EXISTS idx_jrdb_entries_race_date ON jrdb_race_entries(race_date);
    CREATE INDEX IF NOT EXISTS idx_jrdb_entries_blood_no ON jrdb_race_entries(blood_registration_no);